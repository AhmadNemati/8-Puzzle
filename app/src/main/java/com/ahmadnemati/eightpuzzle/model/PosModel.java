public class PosModel {
    private String farmer;
    private String wolf;
    private String goat;
    private String cabbage;



    public String getFarmer() {
        return farmer;
    }

    public void setFarmer(String farmer) {
        this.farmer = farmer;
    }

    public String getWolf() {
        return wolf;
    }

    public void setWolf(String wolf) {
        this.wolf = wolf;
    }

    public String getGoat() {
        return goat;
    }

    public void setGoat(String goat) {
        this.goat = goat;
    }

    public String getCabbage() {
        return cabbage;
    }

    public void setCabbage(String cabbage) {
        this.cabbage = cabbage;
    }
}
